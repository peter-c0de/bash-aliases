# Requirements

# xsel
# command-line tool to access X clipboard and selection buffers
sudo apt-get install xsel

# yt-dlp
# downloader of videos from YouTube and other sites
# Install:
python3 -m pip install -U yt-dlp
# Update:
pip3 install --upgrade yt-dlp 

# FFmpeg
# a suite of libraries and programs for handling video, audio, and other multimedia files and streams.
sudo apt-get install ffmpeg

# imagemagick
# image manipulation programs -- binaries
sudo apt-get install imagemagick

# poppler-utils
# PDF utilities (based on Poppler)
sudo apt-get install poppler-utils

# pdftoppm
# PDF utilities (based on Poppler)
sudo apt-get install pdftoppm

# xpdf
# Motif-based PDF reader using the Poppler library
sudo apt-get install xpdf






